import gymnasium as gym
import numpy as np
from numpy import ndarray
from typing import List, Tuple, Dict, Optional
from scipy.stats import norm, uniform

class RacetrackEnv(gym.Env):
    def __init__(self, track_index: int = 0, step_limit: int = 200, render_mode: str = None):
        super().__init__()

        self.agent_location: ndarray[float] = np.array([-1, -1], dtype=np.float32)
        self.agent_speed: ndarray[float] = np.array([0, 0], dtype=np.float32)
        self.track_width: int = -1
        self.track_height: int = -1
        
        self.start_cells: List[Tuple[int, int]] = []
        self.wall_cells: List[Tuple[int, int]] = []
        self.goal_cells: List[Tuple[int, int]] = []

        self.step_limit: int = step_limit
        self.step_count: int = 0

        self.intilialize_track_info(track_index)

        self.observation_space = gym.spaces.Box(np.array([0, 0, -np.inf, -np.inf]), np.array([np.inf, np.inf, np.inf, np.inf]), shape=(4,), dtype=np.float32)
        self.action_space = gym.spaces.Discrete(9)

        self.action_to_acceleration: Dict[int, ndarray[float]] = {
            0: np.array([-1, -1], dtype=np.float32),
            1: np.array([0, -1], dtype=np.float32),
            2: np.array([1, -1], dtype=np.float32),
            3: np.array([-1, 0], dtype=np.float32),
            4: np.array([0, 0], dtype=np.float32),
            5: np.array([1, 0], dtype=np.float32),
            6: np.array([-1, 1], dtype=np.float32),
            7: np.array([0, 1], dtype=np.float32),
            8: np.array([1, 1], dtype=np.float32),
        }

    def get_track_form_index(self, index: int) -> List[List[int]]:
        if index == 0:
            return TracksList.r_track
        elif index == 1:
            return TracksList.block80
    
    def intilialize_track_info(self, index: int) -> None:
        track_matrix: List[List[int]] = self.get_track_form_index(index)
        self.track_height = len(track_matrix)
        self.track_width = len(track_matrix[0])
        for row in range(self.track_height):
            for col in range(self.track_width):
                cell = track_matrix[row][col]
                if cell == 0:
                    self.wall_cells.append((row, col))
                elif cell == 2:
                    self.start_cells.append((row, col))
                elif cell == 3:
                    self.goal_cells.append((row, col))
    
    def pos_to_cell(self, x: float, y: float) -> Tuple[int, int]:
        row = int(y)
        col = int(x)
        if x < 0:
            col -= 1
        if y < 0:
            row -= 1
        return (row, col)

    def get_cells_passed_by_move(self, x: float, y: float, sx: float, sy: float) -> List[Tuple[int, int]]:
        (row_pos, col_pos) = self.pos_to_cell(x, y)
        (final_row, final_col) = self.pos_to_cell(x + sx, y + sy)

        cells_passed: List[Tuple[int, int]] = [(row_pos, col_pos)]

        drow: int = 1 if sy > 0 else -1
        dcol: int = 1 if sx > 0 else -1

        dist_x: float = 1.0 - (x % 1) if sx > 0 else (x % 1)
        dist_y: float = 1.0 - (y % 1) if sy > 0 else (y % 1)
        while (row_pos, col_pos) != (final_row, final_col):
            if sy == 0:
                col_pos += dcol
            elif sx == 0:
                row_pos += drow
            else:
                time_x: float = dist_x / abs(sx)
                time_y: float = dist_y / abs(sy)
                if time_y < time_x:
                    row_pos += drow
                    dist_x -= abs(sx) * time_y
                    dist_y = 1.0
                    if dist_x == 0.0:
                        col_pos += dcol
                        dist_x = 1.0
                elif time_x < time_y:
                    col_pos += dcol
                    dist_y -= abs(sy) * time_x
                    dist_x = 1.0
                    if dist_y == 0.0:
                        row_pos += drow
                        dist_y = 1.0
                else:
                    row_pos += drow
                    col_pos += dcol
                    dist_x = 1.0
                    dist_y = 1.0
            cells_passed.append((row_pos, col_pos))

            if len(cells_passed) > 100:
                print(row_pos, col_pos)
                print(final_row, final_col)
                print(cells_passed[:10])
        return cells_passed

    def get_obs(self) -> ndarray[float]:
        return np.concat([self.agent_location, self.agent_speed])
    
    def get_info(self) -> Dict:
        return {}

    def reset_agent(self) -> None:
        rand: int = np.random.randint(len(self.start_cells))
        start_cell: Tuple[int, int] = self.start_cells[rand]
        self.agent_location[0] = start_cell[1] + 0.5 # x = col
        self.agent_location[1] = start_cell[0] + 0.5 # y = row
        self.agent_speed[0] = 0.0
        self.agent_speed[1] = 0.0

    '''
    First bool returned is if the movement is possible, and the second bool returned is if the move reaches the goal
    '''
    def is_move_possible(self, x: int, y: int, sx: int, sy: int) -> Tuple[bool, bool]:
        cells_passed: List[Tuple[int, int]] = self.get_cells_passed_by_move(x, y, sx, sy)
        
        for cell in cells_passed:
            if cell in self.goal_cells:
                return (True, True)
            if cell in self.wall_cells:
                return (False, False)
            if cell[0] < 0 or cell[1] < 0 or cell[0] >= self.track_height or cell[1] >= self.track_width:
                return (False, False)
        return (True, False)

    def reset(self, seed: Optional[int] = None, options: Optional[dict] = None):
        super().reset(seed=seed)

        self.step_count = 0
        self.reset_agent()
        obs = self.get_obs()
        info = self.get_info()
        return obs, info
    


    def step(self, action: int):
        rand: float = np.random.random()
        acceleration: ndarray[float] = np.array([0.0, 0.0], dtype=np.float32)
        if rand > 0.1:
            acceleration = self.action_to_acceleration[action].copy()
            acceleration += norm.rvs(loc=0.0, scale=0.04, size=2)
        
        #print("a", acceleration)
        self.agent_speed = self.agent_speed + acceleration
        #print("s", self.agent_speed)
        (can_move, reaches_goal) = self.is_move_possible(self.agent_location[0], self.agent_location[1], self.agent_speed[0], self.agent_speed[1])

        terminated: bool = False
        reward: float = -1.0
        if reaches_goal:
            terminated = True
            reward = 0.0
        elif can_move:
            self.agent_location = self.agent_location + self.agent_speed
        else:
            self.reset_agent()
        
        self.step_count += 1
        truncated: bool = False
        if (not terminated) and self.step_count >= self.step_limit:
            truncated = True
        
        obs = self.get_obs()
        info = self.get_info()
        return obs, reward, terminated, truncated, info






